function getQueryParam(param) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(param);
}

let carName = getQueryParam('car');
let currentImageIndex = 0;

if (carName && cars[carName]) {
    let car = cars[carName];

    document.getElementById('car-title').innerText = car.title;
    document.getElementById('car-description').innerText = car.description;
    document.getElementById('car-image').src = car.images[currentImageIndex];

    setInterval(() => nextImage(), 10000);
} else {
    document.body.innerHTML = "<h2>Car not found</h2>";
}

function nextImage() {
    let car = cars[carName];
    currentImageIndex = (currentImageIndex + 1) % car.images.length;
    document.getElementById('car-image').src = car.images[currentImageIndex];
}

function prevImage() {
    let car = cars[carName];
    currentImageIndex = (currentImageIndex - 1 + car.images.length) % car.images.length;
    document.getElementById('car-image').src = car.images[currentImageIndex];
}